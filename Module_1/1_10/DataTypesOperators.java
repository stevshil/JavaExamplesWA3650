public class DataTypesOperators {
    public static void main(String[] args) {
        int age = 30;
        double price = 99.99;
        boolean isAdult = age >= 18;
        String name = "John Doe";

        System.out.println("Name: " + name + ", Age: " + age + ", Price: " + price + ", Is Adult: " + isAdult);

        for (int i = 0; i < 5; i++) {
            System.out.println("i: " + i);
        }
    }
}